/****************************************************************************
* 名称：GUI_PutChar8_8()
* 功能：显示ASCII码(8*8字体)，显示值为20H-7FH(若为其它值，则显示' ')。
* 入口参数： x		指定显示位置，x坐标
*           y		指定显示位置，y坐标
*           ch		要显示的ASCII码值。
* 出口参数：返回值为1时表示操作成功，为0时表示操作失败。
* 说明：操作失败原因是指定地址超出有效范围。
****************************************************************************/
U8  GUI_PutChar8_8(U32 x, U32 y, U8 ch)
{  
   U8   font_dat;
   U8   i, j;
   TCOLOR  bakc;

   /* 参数过滤 */
   if( x>(GUI_LCM_XMAX-8) ) return(0);
   if( y>(GUI_LCM_YMAX-8) ) return(0);
   if( (ch<0x20) || (ch>0x7f) ) ch = 0x20;
   
   ch -= 0x20; 
   for(i=0; i<8; i++)
   {  /* 读取点阵数据 */
      font_dat = FONT8x8ASCII[ch][i];
      
      for(j=0; j<8; j++)
      {  /* 设置相应的点为color或为back_color */
         if( (font_dat&DCB2HEX_TAB[j])==0 ) GUI_CopyColor(&bakc, back_color);
            else  GUI_CopyColor(&bakc, disp_color);
         PutPixel(x, y, bakc);       
         x++;
      }
      
      y++;									// 指向下一行
      x -= 8;								// 恢复x值
   }
   
   return(1);
}



/****************************************************************************
* 名称：GUI_PutString8_8()
* 功能：输出显示字符串((8*8字体，没有自动换行功能)。
* 入口参数： x		指定显示位置，x坐标
*           y		指定显示位置，y坐标
*           str		要显示的ASCII码字符串
* 出口参数：无
* 说明：操作失败原因是指定地址超出有效范围。
****************************************************************************/
void  GUI_PutString8_8(U32 x, U32 y, char *str)
{  
  while(1)
   {  if( (*str)=='\0' ) break;
      if( GUI_PutChar8_8(x, y, *str++)==0 ) break;
      x += 6;								// 下一个字符显示位置，y不变(即不换行)
   }
}


/****************************************************************************
* 名称：GUI_PutNoStr8_8()
* 功能：输出显示字符串((8*8字体，没有自动换行功能)，若显示的字符个数大于指定个数，则直接退出。
* 入口参数： x		指定显示位置，x坐标
*           y		指定显示位置，y坐标
*           str		要显示的ASCII码字符串。
*           no      最大显示字符的个数
* 出口参数：无
* 说明：操作失败原因是指定地址超出有效范围。
****************************************************************************/
void  GUI_PutNoStr8_8(U32 x, U32 y, char *str, U8 no)
{  
   if(no==0) return;
   
   for(; no>0; no--)
   {  
      if( (*str)=='\0' ) break;
      if( GUI_PutChar(x, y, *str++)==0 ) break;
      x += 6;								// 下一个字符显示位置，y不变(即不换行)
   }
}

